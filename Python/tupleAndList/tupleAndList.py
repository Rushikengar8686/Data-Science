# Create List
list = [1,2,3,4,5]
# to append element in the list 
list.append(4)
print(list)

# to print element 1 to 4 index
printOneToFour = list[1:4]
print(printOneToFour)

# Print element 1 to last
print("Print element one to last",list[1:])

# print element last to one
print("print element last to one",list[:5])

# count of the occurance of element in this list
listOne = [1,2,3,3,4,5,6,4,4]
print("count 4 ocurance present in this list :::",listOne.count(4))






